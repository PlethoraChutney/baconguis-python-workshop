compatible_users = {}
for individual in users:
    individual_party = individual['political_party']
    
    for party in willing_to_date.keys():
        if party not in compatible_users.keys():
            compatible_users[party] = []
        
        if individual_party in willing_to_date[party] or individual_party == 'Nonaffiliated':
            compatible_users[party].append(individual)

user_pairings = {}
for individual in users:
    # use individual[party] to pull the list of compatible users from compatible_users
    user_party = individual['political_party']

    # pull the list of people our user is willing to date
    # this is everyone if nonaffiliated
    if user_party != 'Nonaffiliated':
        maybe_willing_to_date = compatible_users[user_party]
    else:
        maybe_willing_to_date = users

    willing_to_date = []
    for potential_mate in maybe_willing_to_date:
        if individual != potential_mate:
            willing_to_date.append(potential_mate)

    user_pairings[individual['user_id']] = willing_to_date


