party_compatibility = {
    'Democrat': ['Democrat', 'Independent'],
    'Republican': ['Republican'],
    'Independent': ['Democrat', 'Independent'],
    'Libertarian': ['Libertarian'],
    'Democratic-Socialist': ['Democratic-Socialist'],
    'Nonaffiliated': []
}

# this will add all the political parties to the NA compatibility
# without us having to know the political parties ahead of
# time
for party in party_compatibility:
    party_compatibility['Nonaffiliated'].append(party)

    # add NA to all compatibility except NA (since it is added in the first line)
    if party != 'Nonaffiliated':
        party_compatibility[party].append('Nonaffiliated')

# generate a dictionary of users by political party
users_by_pol_party = {}
for user in users:
    party = user['political_party']

    if party not in users_by_pol_party:
        users_by_pol_party[party] = []
    
    users_by_pol_party[party].append(user)

# use the above dict to generate the date lists for
# each user, and send them on dates

for user in users:
    willing_to_date = []
    for party in party_compatibility[user['political_party']]:
        # extend() is like append(), except instead of adding the
        # object to the list, it adds all the elements of the object
        # to the list
        # 
        # https://docs.python.org/3/tutorial/datastructures.html
        # 
        # so willing_to_date will be a list of users from each political
        # party that our current user is...willing to date
        willing_to_date.extend(users_by_pol_party[party])

    # send the users on a date
    from random import choice, randint

    potential_mate = choice(willing_to_date)
    date_quality = randint(0, 10)

    # check for shared interest
    if user['interests'] == potential_mate['interests']:
        date_quality += 2

    # check political parties
    if user['political_party'] == potential_mate['political_party']:
        date_quality += 1
    else:
        date_quality -= 1

    # check age difference
    if abs(user['age'] - potential_mate['age']) > 4:
        date_quality -= 1
    else:
        date_quality += 1
    
    date_success = date_quality > 5
    print(date_success)